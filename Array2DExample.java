public class Array2DExample {
    public static void main(String[]args) {
        int [][] array2D={{2,4},{6,8}};
        System.out.print((array2D[0][0]));
        System.out.println((array2D[0][1]));
        System.out.print((array2D[1][0]));
        System.out.println((array2D[1][1]));
        
    }
    
}
