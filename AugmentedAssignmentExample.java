public class AugmentedAssignmentExample{
    public static void main(String[]args) {
        int a=20;

        a +=10; // this same with a= a+10
        System.out.println(a);

        a -=10; //this same with a= a-10
        System.out.println(a);

        a *=10;//this same with a= a*10
        System.out.println(a);

        a /=10;//this same with a= a/10
        System.out.println(a);

    }
}